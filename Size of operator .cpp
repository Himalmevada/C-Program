#include<iostream>
using namespace std;
int main()
{
    int i;
    float f;
    char c;
    double d;

    cout<<"Size of char   : "<<sizeof(c)<<" byte"<<endl;
    cout<<"Size of int    : "<<sizeof(i)<<" byte"<<endl;
    cout<<"Size of float  : "<<sizeof(f)<<" byte"<<endl;
    cout<<"Size of double : "<<sizeof(d)<<" byte"<<endl;
    return 0;
}
